 package com.project.Arraycontainer;

public class Arrayboolean {
	public static void main(String[] args) {
		boolean[] boocontainer=new boolean[2];
		
		
		boocontainer[0]=true;
		boocontainer[1]=false;
		
		
		for(boolean boo:boocontainer) {
			System.out.println(boo);
		}
		
		
	}

}
