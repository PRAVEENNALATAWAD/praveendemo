package com.project.Arraycontainer;

public class Arrayint {
	public static void main(String[] args) {
		int[] intcontainer=new int[5];
		
		
		intcontainer[0]=25;
		intcontainer[1]=25;
		intcontainer[2]=25;
		intcontainer[3]=25;
		intcontainer[4]=25;
		
		for(int in:intcontainer) {
			System.out.println(in);
			
		}
		
		

		}

}
