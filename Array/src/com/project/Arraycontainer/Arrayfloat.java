package com.project.Arraycontainer;

public class Arrayfloat {
	public static void main(String[] args) {
		float[] fltcontainer=new float[6];
		
		
		fltcontainer[0]=85.63f;
		fltcontainer[1]=857.36f;
		fltcontainer[2]=25.36f;
		fltcontainer[3]=22.89f;
		fltcontainer[4]=2536.63f;
		fltcontainer[5]=27.83f;
		
		for(float flt:fltcontainer) {
			System.out.println(flt);
		}
		
		
	}

}
